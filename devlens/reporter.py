def _score_color(score: int) -> str:
    if score >= 90: return "var(--green)"
    if score >= 75: return "#56d364"
    if score >= 60: return "var(--yellow)"
    if score >= 40: return "var(--orange)"
    return "var(--red)"


def _grade_letter(grade: str) -> str:
    return grade[0].upper() if grade else "?"


def _sev_html(severity: str) -> str:
    return f'<span class="sev sev-{severity.lower()}">{severity}</span>'


def generate_html(data: ReportData) -> str:
    """Generate a full HTML dashboard report."""
    parts = []

    # Review section
    if data.review:
        r = data.review
        grade_letter = _grade_letter(r.grade)
        color = _score_color(r.score)
        parts.append(f"""
  <h2>Code Review</h2>
  <div class="score-row">
    <div class="grade-badge grade-{grade_letter}">{r.grade}</div>
    <div style="flex:1">
      <div style="font-size:1.1rem">Score: <strong>{r.score}/100</strong> — Risk: {r.risk_level}</div>
      <div class="score-bar"><div class="score-fill" style="width:{r.score}%;background:{color}"></div></div>
    </div>
  </div>""")

        if r.findings:
            rows = ""
            for i, f in enumerate(r.findings, 1):
                sev = f.get("severity", "medium")
                cat = f.get("category", "general")
                title = html.escape(f.get("title", f.get("finding", "")))
                rows += f"<tr><td>{i}</td><td>{_sev_html(sev)}</td><td>{cat}</td><td>{title}</td></tr>\n"
            parts.append(f"""
  <table>
    <thead><tr><th>#</th><th>Severity</th><th>Category</th><th>Finding</th></tr></thead>
    <tbody>{rows}</tbody>
  </table>""")

        if r.summary:
            parts.append(f'<div class="ai-box">{html.escape(r.summary)}</div>')

    # Security section
    if data.scan_result:
        scan = data.scan_result
        grade_letter = _grade_letter(scan.grade)
        color = _score_color(scan.score)
        
        crit = scan.critical_count
        high = scan.high_count
        med = scan.medium_count
        low = scan.low_count
        
        parts.append(f"""
  <h2>Security Scan</h2>
  <div class="score-row">
    <div class="grade-badge grade-{grade_letter}">{scan.grade}</div>
    <div style="flex:1">
      <div style="font-size:1.1rem">Score: <strong>{scan.score}/100</strong></div>
      <div class="score-bar"><div class="score-fill" style="width:{scan.score}%;background:{color}"></div></div>
    </div>
  </div>
  <div class="stats">
    <div class="stat-card"><div class="stat-value" style="color:var(--red)">{crit}</div><div class="stat-label">Critical</div></div>
    <div class="stat-card"><div class="stat-value" style="color:var(--orange)">{high}</div><div class="stat-label">High</div></div>
    <div class="stat-card"><div class="stat-value" style="color:var(--yellow)">{med}</div><div class="stat-label">Medium</div></div>
    <div class="stat-card"><div class="stat-value" style="color:var(--accent)">{low}</div><div class="stat-label">Low</div></div>
    <div class="stat-card"><div class="stat-value">{scan.files_scanned}</div><div class="stat-label">Files Scanned</div></div>
  </div>""")

        if scan.findings:
            rows = ""
            for i, f in enumerate(scan.findings, 1):
                rows += (
                    f"<tr><td>{i}</td><td>{_sev_html(f.severity.value)}</td>"
                    f"<td>{f.rule_id}</td><td><code>{html.escape(f.file)}</code></td>"
                    f"<td>{html.escape(f.title)}</td></tr>\n"
                )
            parts.append(f"""
  <table>
    <thead><tr><th>#</th><th>Severity</th><th>Rule</th><th>File</th><th>Description</th></tr></thead>
    <tbody>{rows}</tbody>
  </table>""")

        if scan.ai_summary:
            parts.append(f'<div class="ai-box">{html.escape(scan.ai_summary)}</div>')

    # Summary section
    if data.summary:
        s = data.summary
        cats = ", ".join(f"<code>{c}</code>" for c in s.categories) if s.categories else "general"
        bullets = "".join(f"<li>{html.escape(c)}</li>" for c in s.key_changes)
        parts.append(f"""
  <h2>PR Summary</h2>
  <p><strong>Categories:</strong> {cats} | <strong>Impact:</strong> {s.impact}</p>
  <p>{html.escape(s.overview)}</p>
  <ul>{bullets}</ul>""")

    content = "\n".join(parts)

    return HTML_TEMPLATE.format(
        pr_number=data.pr_number,
        pr_title=html.escape(data.pr_title),
        repo=html.escape(data.repo),
        timestamp=data.timestamp,
        content=content,
    )


def export_report(
    data: ReportData,
    output_path: str,
    fmt: str = "auto",
) -> str:
    """Export report to file. Returns the path written.
    
    fmt: 'auto' (detect from extension), 'md', 'html'
    """
    path = Path(output_path)

    if fmt == "auto":
        fmt = "html" if path.suffix.lower() in (".html", ".htm") else "md"

    if fmt == "html":
        content = generate_html(data)
    else:
        content = generate_markdown(data)

    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")
    return str(path)
