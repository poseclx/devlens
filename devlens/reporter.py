"""Report generator — Markdown and HTML export for DevLens results.

Generates dashboard-style reports with:
  - Grade badge and score
  - Findings summary table
  - Category breakdown chart (ASCII in MD, CSS in HTML)
  - AI analysis section
  - Metadata (timestamp, PR info)
"""

from __future__ import annotations

import html
import json
from datetime import datetime, timezone
from dataclasses import dataclass
from pathlib import Path

from devlens.analyzer import ReviewResult


@dataclass
class ReportData:
    """Unified data container for report generation."""
    pr_number: int
    pr_title: str
    repo: str
    review: ReviewResult | None = None
    scan_result: object | None = None  # ScanResult (avoid circular import)
    summary: object | None = None      # PRSummary
    timestamp: str = ""

    def __post_init__(self):
        if not self.timestamp:
            self.timestamp = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")


# ── Markdown Report ───────────────────────────────────────────

def _severity_icon(severity: str) -> str:
    icons = {"critical": "🔴", "high": "🟠", "medium": "🟡", "low": "🔵", "info": "⚪"}
    return icons.get(severity.lower(), "⚪")


def generate_markdown(data: ReportData) -> str:
    """Generate a full Markdown report."""
    lines = [
        f"# DevLens Report — PR #{data.pr_number}",
        f"**{data.pr_title}**  ",
        f"Repository: `{data.repo}` | Generated: {data.timestamp}",
        "",
        "---",
        "",
    ]

    # Review section
    if data.review:
        r = data.review
        lines.extend([
            "## Code Review",
            "",
            f"**Grade: {r.grade}** | Score: {r.score}/100 | Risk: {r.risk_level}",
            "",
        ])
        if r.findings:
            lines.append("### Findings\n")
            lines.append("| # | Severity | Category | Finding |")
            lines.append("|---|----------|----------|---------|")
            for i, f in enumerate(r.findings, 1):
                sev = f.get("severity", "medium")
                cat = f.get("category", "general")
                title = f.get("title", f.get("finding", ""))
                lines.append(f"| {i} | {_severity_icon(sev)} {sev} | {cat} | {title} |")
            lines.append("")

        if r.summary:
            lines.extend(["### AI Analysis", "", r.summary, ""])

    # Security scan section
    if data.scan_result:
        scan = data.scan_result
        lines.extend([
            "## Security Scan",
            "",
            f"**Grade: {scan.grade}** | Score: {scan.score}/100",
            f"Files scanned: {scan.files_scanned}/{scan.total_files}",
            "",
        ])
        if scan.findings:
            lines.append("### Security Findings\n")
            lines.append("| # | Severity | Rule | File | Description |")
            lines.append("|---|----------|------|------|-------------|")
            for i, f in enumerate(scan.findings, 1):
                lines.append(
                    f"| {i} | {_severity_icon(f.severity.value)} {f.severity.value} "
                    f"| {f.rule_id} | `{f.file}` | {f.title} |"
                )
            lines.append("")

        if scan.ai_summary:
            lines.extend(["### AI Security Assessment", "", scan.ai_summary, ""])

    # Summary section
    if data.summary:
        lines.extend(["## PR Summary", "", data.summary.to_markdown(), ""])

    lines.extend(["---", f"*Generated by [DevLens](https://github.com/poseclx/devlens) v0.3.0*"])

    return "\n".join(lines)


